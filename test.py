# Example of changing the size of an array
my_array = [1, 2, 3, 4, 5]
print("Original array:", my_array)

# Increase the size of the array by appending elements
my_array.append(6)
my_array.extend([7, 8])
print("Increased size:", my_array)

# Decrease the size of the array by removing elements
my_array = my_array[:3]
print("Decreased size:", my_array)




    